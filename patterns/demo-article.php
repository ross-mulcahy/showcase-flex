<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">The implications ripple outward from coastlines into every aspect of civic life: property insurance markets, public transit planning, school siting, hospital emergency preparedness, and the basic question of where a city invests its infrastructure dollars. In Norfolk, Virginia — home to the world's largest naval base — the Department of Defense has spent $21 billion on climate-adaptation measures since 2018, a figure that dwarfs the city's own annual budget.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">What makes the current moment different from previous climate policy debates is the sheer resolution of the data now available. Machine-learning models trained on petabytes of satellite imagery, ocean-buoy telemetry, and atmospheric reanalysis datasets can now project block-by-block flood risk with accuracy that was unimaginable a decade ago. For city planners, the abstraction is gone. The map is the territory.</p>
<!-- /wp:paragraph -->

