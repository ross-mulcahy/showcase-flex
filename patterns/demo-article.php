<!-- wp:heading {"level":2,"className":"sf-section-header sf-animate sf-animate--fade-up"} -->
<h2 class="wp-block-heading sf-section-header sf-animate sf-animate--fade-up">IV. Engineering the Response</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">The response to coastal flooding has historically followed a predictable pattern: build higher walls, pump more water, and hope the problem stays manageable. But a growing cohort of engineers, urban planners, and policymakers is embracing a fundamentally different philosophy — one rooted in adaptation rather than resistance.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">Rotterdam, the Netherlands, has become the global exemplar. A city where 90 percent of the population lives below sea level has turned its existential vulnerability into an innovation engine. Water plazas that serve as basketball courts in dry weather become retention basins during storms. Green roofs absorb rainfall across the city center. Floating neighbourhoods in the Nassauhaven basin demonstrate that urban density need not be tied to dry land at all.</p>
<!-- /wp:paragraph -->

