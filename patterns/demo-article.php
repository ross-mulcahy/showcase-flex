<h2 class="wp-block-heading sf-section-header sf-animate sf-animate--fade-up">I. The Warning No One Wanted to Hear</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">The memo arrived on a Tuesday, buried in a stack of quarterly briefings that most city council members would skim at best. But Dr. Sarah Okonkwo, the recently appointed chief resilience officer for the city of Charleston, South Carolina, read every page. What she found changed the trajectory of a city — and, eventually, the national conversation about climate adaptation.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">The document, prepared by NOAA's Coastal Services Center, contained revised flood-risk projections for the southeastern United States. The numbers were startling: areas previously classified as low-risk would experience chronic tidal flooding within fifteen years — not fifty, as earlier models had predicted. The acceleration was driven by a convergence of factors that scientists had modeled independently but rarely examined together: thermal expansion of seawater, accelerating ice-sheet melt in Greenland and West Antarctica, and subsidence of the coastal land itself.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">"We had been planning for a future that was already here," Okonkwo told us in an interview at her office overlooking the Cooper River. "The data didn't just move the goalposts — it told us we'd been playing on the wrong field entirely."</p>
<!-- /wp:paragraph -->

