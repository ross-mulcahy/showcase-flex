<h2 class="wp-block-heading sf-section-header sf-animate sf-animate--fade-up">III. The Human Cost of Rising Water</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">Behind every data point is a community. In the Chesterfield Heights neighbourhood of Norfolk, lifelong residents describe a slow-motion crisis that statistics can't fully capture. Streets that flooded once or twice a year now flood more than a dozen times. Cars stall in brackish water on morning commutes. Children wade through puddles to reach school buses. Basements that once stored family heirlooms are now perpetually damp, the drywall swollen and crumbling.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">"You learn to live with it, and then you learn that you can't," says Denise Williams, a retired schoolteacher who has lived on Filmore Street for thirty-seven years. "My house was worth $180,000 in 2015. I got it appraised last year — $94,000. And the insurance company dropped us."</p>
