<h2 class="wp-block-heading sf-section-header sf-animate sf-animate--fade-up">II. The Data That Changed Everything</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">Understanding the scale of the problem requires understanding the data itself. For decades, coastal flood projections relied on linear extrapolations from tide-gauge records — some stretching back more than a century. These gauges, mechanical instruments bolted to piers and seawalls, captured a steady and seemingly manageable trend: roughly three millimeters of sea-level rise per year globally.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"sf-animate sf-animate--fade-up"} -->
<p class="sf-animate sf-animate--fade-up">But satellite altimetry, which became the gold standard for ocean measurement in the mid-1990s, told a different story. The Jason series of satellites, orbiting 1,336 kilometers above the Earth's surface, revealed that the rate of rise was itself accelerating. Between 1993 and 2023, the average rate jumped to 3.6 millimeters per year — and in the most recent decade, it exceeded 4.4 millimeters annually. These may sound like small numbers. They are not.</p>
<!-- /wp:paragraph -->

